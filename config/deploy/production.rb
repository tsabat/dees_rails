# Simple Role Syntax
# ==================
# Supports bulk-adding hosts to roles, the primary
# server in each group is considered to be the first
# unless any hosts have the primary property set.
# Don't declare `role :all`, it's a meta role
role :app, %w{ubuntu@ubuntu@ec2-54-202-85-89.us-west-2.compute.amazonaws.com}
role :web, %w{ubuntu@ubuntu@ec2-54-202-85-89.us-west-2.compute.amazonaws.com}
role :db,  %w{ubuntu@ubuntu@ec2-54-202-85-89.us-west-2.compute.amazonaws.com}

